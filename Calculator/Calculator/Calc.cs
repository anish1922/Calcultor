using System;
using System.Collections.Generic;
using System.Text;

namespace Calculator
{
    public class Calc
    {
        public int Add(int x, int y)
        {
            return x + y;
        }

        public int Subtract(int x, int y)
        {

            return x - y;

        }

        public int Mul(int x, int y)
        {
            return x * y;
        }
        public int Div(int x, int y)
        {
            return x / y;
        }
    }
}
